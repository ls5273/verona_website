                <h2>Municipal walls</h2>
                <p>Verona's municipal walls were medieval defensive structures built in the 12th century south of the historic center, near the Adigetto. Remnants can still be seen along Via Pallone and inside Castelvecchio, though many original gates have been altered over time. Strengthened under Ezzelino III da Romano, the walls featured a fortification system with walls, antemurals, and ditches for layered defense. As Cangrande I della Scala expanded the city’s main walls, these defenses lost their strategic role and evolved into a military complex with barracks, a hospital, a granary, and other facilities. The Grand Guard palace became the system's centerpiece, intended to house a military academy, though it was never completed.</p>
                <p>&#128205; Via Pallone, 7, 37121, Verona, Italy</p>
        
