                <h2>Casa di Giulietta</h2>
                <p>Although Romeo and Juliet are fictional characters and neither of them were ever in Verona, there is a beautiful tourist attraction: <strong>Juliet’s house (Casa di Giulietta)</strong>. </p>
                <p>Juliet’s house is one of the most famous places in the city of Verona. It is a beautiful medieval palace where a family named Dal Cappello probably lived since the 13th century - a name similar enough to the Capulet name to <strong>spark a legend</strong>. The legend that Juliet actually lived in the house became so strongly tied to the building that in 1907 the city of Verona bought part of the property to create a <strong>museum and house dedicated to the great love story</strong>. The house features a picturesque and romantic balcony on which Juliet set foot looking for Romeo in the play. The power of association is strong and countless visitors have made the journey to Juliet’s House to gaze up at the balcony, visit the small museum dedicated to Shakespeare’s famous play, and to experience the romantic atmosphere. There is a <strong>bronze statue of Juliet</strong> in the yard of the house and the <strong>local legend says</strong> that if a female touches the hand of the statue of Juliet, she will be blessed with a happy and lasting love that Romeo and Juliet were unable to enjoy. For males, the same will be if they touch both the hand and the breast. Otherwise, the person will be cursed with an unhappy love.</p>

